namespace MyBoilerPlate.Tests.Repositories
{
    [TestClass]
    public class FakeRepositoryTest : TestBase
    {
        private IDataRepositoryFactory _DataRepositoryFactory;

        [TestInitialize]
        public void Setup()
        {
            this.Initialize(new List<Type>()
            {
                typeof(HttpContextInstaller),
                typeof(DataInstaller),
                typeof(FakeRepositoryInstaller)
            });

            _DataRepositoryFactory = ObjectContainer.GetService<IDataRepositoryFactory>();
        }

        [TestMethod]
        public async Task FakeAutogeneratedIds()
        {
            // Arrange
            var employees = new List<Employee>()
            {
                new Employee { FirstName = "Employee A"},
                new Employee { FirstName = "Employee B"},
                new Employee { FirstName = "Employee C"},
                new Employee { FirstName = "Employee D"},
            };

            var repo = _DataRepositoryFactory.GetDataRepository<Employee>();

            // Act
            await repo.AddRangeAsync(employees);

            // Assert
            foreach(var item in employees)
            {
                Assert.IsTrue(item.Id > 0);
            }
        }

        [TestMethod]
        public void GetUnitOfWork()
        {
            var unitOfWork = _DataRepositoryFactory.GetUnitOfWork();

            using (var transaction = unitOfWork.CreateTransaction())
            {
                transaction.Commit();
            }

            Assert.IsNull(null);
        }
    }
}
